d = input('Digite algo: ')
print('\033[30mTipo \033[m{}'.format(type(d)))
print('\033[31mÉ numérico? \033[m{}'.format(d.isnumeric()))
print('\033[32mÉ alfabético? \033[m{}'.format(d.isalpha()))
print('\033[33mÉ alfanumérico? \033[m{}'.format(d.isalnum()))
print('\033[34mEstá apenas em letras maiúsculas? \033[m{}'.format(d.isupper()))
print('\033[35mEstá apenas em letras minúsculas? \033[m{}'.format(d.islower()))
print('\033[36mContém apenas espaços? \033[m{}'.format(d.isspace()))
print('\033[37mEstá capitalizada? \033[m{}'.format(d.istitle()))